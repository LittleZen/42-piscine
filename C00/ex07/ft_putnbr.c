#include <unistd.h>

void	ft_putchar(char b)
{
	write(1, &b, 1);
}

void	ft_putnbr(int nb)
{
	char i;

	i = nb;
	if (i < 0)
	{
		ft_putnbr('-');
		i = nb * (-1);
	}
	if (i > 9)
	{
		ft_putnbr(i / 10);
		ft_putnbr(i % 10);
	}
	else
	{
		ft_putchar(i + '0');
	}
}
